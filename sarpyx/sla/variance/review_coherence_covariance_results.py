#!/usr/bin/env python3
"""
Create plots and rankings from a coherence/covariance metrics workbook.

The input workbook must be generated by compute_coherence_covariance_metrics.py.
The script ranks configurations using complementarity and inverse normalized
spatial variability, saves per-polarization and aggregated plots, and writes the
ranking tables to an Excel workbook.

By default, L2 configurations are excluded. Use --include-l2 to keep them.

Usage:
    python review_coherence_covariance_results.py --metrics-xlsx coherence_covariance_metrics.xlsx --output-dir results_review
"""

from __future__ import annotations

import argparse
from pathlib import Path

import numpy as np
import pandas as pd

adjust_text = None
HAS_ADJUSTTEXT = False


MARKERS = {
    3: "o",
    5: "s",
    7: "^",
    9: "D",
    15: "P",
}


def minmax_norm(s: pd.Series) -> pd.Series:
    s = pd.Series(s, dtype="float64")
    out = pd.Series(np.nan, index=s.index, dtype="float64")
    valid = s.notna() & np.isfinite(s)

    if valid.sum() == 0:
        return out

    vmin = s[valid].min()
    vmax = s[valid].max()

    if np.isclose(vmin, vmax):
        out.loc[valid] = 1.0
    else:
        out.loc[valid] = (s.loc[valid] - vmin) / (vmax - vmin)

    return out


def load_optional_adjust_text() -> None:
    global HAS_ADJUSTTEXT, adjust_text

    try:
        from adjustText import adjust_text as imported_adjust_text
    except ImportError:  # pragma: no cover - optional plotting nicety.
        HAS_ADJUSTTEXT = False
        adjust_text = None
    else:
        HAS_ADJUSTTEXT = True
        adjust_text = imported_adjust_text


def numeric_suffix(series: pd.Series) -> pd.Series:
    return pd.to_numeric(series.astype(str).str.extract(r"(\d+)", expand=False), errors="coerce")


def load_summary(metrics_xlsx: Path, exclude_l2: bool) -> pd.DataFrame:
    summary_df = pd.read_excel(metrics_xlsx, sheet_name="summary")
    required = {
        "kind",
        "pol",
        "lookset",
        "window",
        "selected_bands",
        "mean_nmi",
        "complementarity_1_minus_mean_nmi",
    }
    missing = required - set(summary_df.columns)
    if missing:
        raise ValueError(f"Missing columns in summary sheet: {sorted(missing)}")

    summary_df["L_num"] = numeric_suffix(summary_df["lookset"])
    summary_df["W_num"] = numeric_suffix(summary_df["window"])
    summary_df = summary_df.dropna(subset=["L_num", "W_num"]).copy()
    summary_df["L_num"] = summary_df["L_num"].astype(int)
    summary_df["W_num"] = summary_df["W_num"].astype(int)

    if exclude_l2:
        summary_df = summary_df[summary_df["L_num"] > 2].copy()

    return summary_df.sort_values(["kind", "pol", "L_num", "W_num"]).reset_index(drop=True)


def build_metrics_df(
    summary_df: pd.DataFrame,
    weight_complementarity: float,
    weight_variability: float,
) -> pd.DataFrame:
    rows = []

    for _, row in summary_df.iterrows():
        if row["kind"] == "coherence":
            variability = row.get("coherence_median_cv", np.nan)
            variability_name = "coherence_median_cv"
        elif row["kind"] == "covariance":
            variability = row.get("covariance_diag_median_cv", np.nan)
            variability_name = "covariance_diag_median_cv"
        else:
            continue

        rows.append(
            {
                "kind": row["kind"],
                "pol": row["pol"],
                "lookset": row["lookset"],
                "window": row["window"],
                "L_num": row["L_num"],
                "W_num": row["W_num"],
                "config": f"L{row['L_num']}/W{row['W_num']}",
                "selected_bands": row["selected_bands"],
                "mean_nmi": row["mean_nmi"],
                "complementarity": row["complementarity_1_minus_mean_nmi"],
                "variability": variability,
                "variability_name": variability_name,
                "coherence_median_cv": row.get("coherence_median_cv", np.nan),
                "covariance_diag_median_cv": row.get("covariance_diag_median_cv", np.nan),
            }
        )

    metrics_df = pd.DataFrame(rows)
    if metrics_df.empty:
        return metrics_df

    metrics_df = metrics_df[
        metrics_df["complementarity"].notna() & metrics_df["variability"].notna()
    ].copy()

    metrics_df["comp_norm"] = np.nan
    metrics_df["var_norm"] = np.nan

    for (_kind, _pol), idx in metrics_df.groupby(["kind", "pol"]).groups.items():
        idx = list(idx)
        metrics_df.loc[idx, "comp_norm"] = minmax_norm(metrics_df.loc[idx, "complementarity"])
        metrics_df.loc[idx, "var_norm"] = minmax_norm(metrics_df.loc[idx, "variability"])

    metrics_df["inv_var_norm"] = 1.0 - metrics_df["var_norm"]
    metrics_df["score"] = (
        weight_complementarity * metrics_df["comp_norm"]
        + weight_variability * metrics_df["inv_var_norm"]
    )

    return metrics_df


def build_kind_aggregation(metrics_df: pd.DataFrame) -> pd.DataFrame:
    if metrics_df.empty:
        return pd.DataFrame()

    return (
        metrics_df.dropna(subset=["score"])
        .groupby(["kind", "L_num", "W_num", "lookset", "window"], as_index=False)
        .agg(
            complementarity=("complementarity", "mean"),
            variability=("variability", "mean"),
            comp_norm=("comp_norm", "mean"),
            var_norm=("var_norm", "mean"),
            inv_var_norm=("inv_var_norm", "mean"),
            score=("score", "mean"),
            selected_bands=("selected_bands", "mean"),
            n_polarizations=("pol", "nunique"),
            n_components=("score", "count"),
        )
    )


def pareto_mask_maxmax(x: np.ndarray, y: np.ndarray) -> np.ndarray:
    x = np.asarray(x, dtype=float)
    y = np.asarray(y, dtype=float)
    mask = np.ones(len(x), dtype=bool)

    for i in range(len(x)):
        if not np.isfinite(x[i]) or not np.isfinite(y[i]):
            mask[i] = False
            continue

        dominated = np.any((x >= x[i]) & (y >= y[i]) & ((x > x[i]) | (y > y[i])))
        if dominated:
            mask[i] = False

    return mask


def scatter_panel(
    ax,
    df: pd.DataFrame,
    title: str,
    x_col: str = "comp_norm",
    y_col: str = "inv_var_norm",
    score_col: str = "score",
    top_n: int = 5,
    annotate_pareto: bool = False,
    table_fontsize: float = 8.0,
    title_fontsize: float = 11.0,
):
    df = df.copy()
    df = df[df[x_col].notna() & df[y_col].notna() & df[score_col].notna()].copy()

    if len(df) == 0:
        ax.set_title(title)
        ax.text(0.5, 0.5, "No valid data", ha="center", va="center")
        return None

    last_sc = None
    for window, group in df.groupby("W_num"):
        marker = MARKERS.get(int(window), "o")
        sizes = 55 + 13 * group["L_num"].values
        last_sc = ax.scatter(
            group[x_col],
            group[y_col],
            c=group[score_col],
            s=sizes,
            marker=marker,
            cmap="viridis",
            vmin=0,
            vmax=1,
            edgecolors="black",
            linewidths=0.65,
            alpha=0.88,
            zorder=2,
        )

    p_mask = pareto_mask_maxmax(df[x_col].values, df[y_col].values)
    pareto = df[p_mask].copy().sort_values(x_col)

    if len(pareto) > 1:
        ax.plot(
            pareto[x_col],
            pareto[y_col],
            linestyle="--",
            linewidth=1.6,
            color="tab:blue",
            alpha=0.85,
            zorder=3,
        )

    ax.scatter(
        pareto[x_col],
        pareto[y_col],
        s=220,
        facecolors="none",
        edgecolors="crimson",
        linewidths=1.6,
        zorder=4,
    )

    best = df.loc[df[score_col].idxmax()]
    ax.scatter(
        [best[x_col]],
        [best[y_col]],
        marker="*",
        s=500,
        c="gold",
        edgecolors="black",
        linewidths=1.1,
        zorder=6,
    )

    dx = -70 if best[x_col] > 0.82 else 12
    ha = "right" if best[x_col] > 0.82 else "left"
    dy = -22 if best[y_col] > 0.82 else 12

    ax.annotate(
        f"Best: L{int(best['L_num'])}, W{int(best['W_num'])}",
        (best[x_col], best[y_col]),
        textcoords="offset points",
        xytext=(dx, dy),
        ha=ha,
        fontsize=8.5,
        fontweight="bold",
        bbox=dict(boxstyle="round,pad=0.25", fc="white", ec="gray", alpha=0.95),
        arrowprops=dict(arrowstyle="-", color="gray", lw=0.8),
        zorder=7,
    )

    if annotate_pareto and len(pareto) > 0:
        texts = []
        for _, row in pareto.iterrows():
            if int(row["L_num"]) == int(best["L_num"]) and int(row["W_num"]) == int(best["W_num"]):
                continue

            texts.append(
                ax.text(
                    row[x_col],
                    row[y_col],
                    f"L{int(row['L_num'])},W{int(row['W_num'])}",
                    fontsize=7.8,
                    zorder=7,
                    bbox=dict(boxstyle="round,pad=0.12", fc="white", ec="none", alpha=0.75),
                )
            )

        if HAS_ADJUSTTEXT and texts:
            adjust_text(
                texts,
                ax=ax,
                arrowprops=dict(arrowstyle="-", color="gray", lw=0.6, alpha=0.7),
                expand_points=(1.2, 1.35),
                expand_text=(1.2, 1.35),
                force_text=(0.35, 0.55),
                force_points=(0.2, 0.3),
            )

    top = df.sort_values(score_col, ascending=False).head(top_n)
    lines = ["Top configurations"]
    for rank, (_, row) in enumerate(top.iterrows(), start=1):
        lines.append(f"{rank}. L{int(row['L_num'])}, W{int(row['W_num'])}: {row[score_col]:.3f}")

    ax.text(
        0.03,
        0.97,
        "\n".join(lines),
        transform=ax.transAxes,
        ha="left",
        va="top",
        fontsize=table_fontsize,
        bbox=dict(boxstyle="round,pad=0.30", fc="white", ec="#bbbbbb", alpha=0.92),
    )

    ax.axvline(0.5, color="gray", linewidth=0.8, linestyle=":", alpha=0.55)
    ax.axhline(0.5, color="gray", linewidth=0.8, linestyle=":", alpha=0.55)
    ax.set_xlim(-0.05, 1.05)
    ax.set_ylim(-0.05, 1.05)
    ax.set_xlabel("Normalized complementarity")
    ax.set_ylabel("Inverse normalized variability")
    ax.set_title(
        f"{title}\nBest: L{int(best['L_num'])}, W{int(best['W_num'])}, score={best[score_col]:.3f}",
        fontweight="bold",
        fontsize=title_fontsize,
    )
    ax.text(
        0.98,
        0.02,
        "upper-right is better",
        transform=ax.transAxes,
        ha="right",
        va="bottom",
        fontsize=7.5,
        alpha=0.75,
    )

    return last_sc


def add_common_legends(fig, df: pd.DataFrame, x_positions: tuple[float, float, float]) -> None:
    import matplotlib.pyplot as plt

    window_values = sorted(df["W_num"].dropna().unique())
    if window_values:
        w_handles = [
            plt.Line2D(
                [0],
                [0],
                marker=MARKERS.get(int(window), "o"),
                color="none",
                markerfacecolor="lightgray",
                markeredgecolor="black",
                markersize=8,
                label=f"W{int(window)}",
            )
            for window in window_values
        ]
        fig.legend(
            handles=w_handles,
            title="Window W",
            loc="lower center",
            bbox_to_anchor=(x_positions[0], 0.055),
            ncol=max(len(w_handles), 1),
            frameon=True,
            fontsize=8,
            title_fontsize=8,
        )

    look_values = sorted(df["L_num"].dropna().unique())
    if look_values:
        examples = [look_values[0], look_values[len(look_values) // 2], look_values[-1]]
        examples = sorted(set(examples))
        l_handles = [
            plt.scatter(
                [],
                [],
                s=55 + 13 * look,
                c="lightgray",
                edgecolors="black",
                label=f"L{int(look)}",
            )
            for look in examples
        ]
        fig.legend(
            handles=l_handles,
            title="Sub-apertures L",
            loc="lower center",
            bbox_to_anchor=(x_positions[1], 0.055),
            ncol=max(len(l_handles), 1),
            frameon=True,
            fontsize=8,
            title_fontsize=8,
        )

    extra_handles = [
        plt.Line2D([0], [0], linestyle="--", color="tab:blue", linewidth=1.6, label="Pareto frontier"),
        plt.Line2D(
            [0],
            [0],
            marker="o",
            color="none",
            markerfacecolor="none",
            markeredgecolor="crimson",
            markeredgewidth=1.6,
            markersize=10,
            label="Pareto-optimal",
        ),
        plt.Line2D(
            [0],
            [0],
            marker="*",
            color="none",
            markerfacecolor="gold",
            markeredgecolor="black",
            markersize=14,
            label="Best score",
        ),
    ]
    fig.legend(
        handles=extra_handles,
        loc="lower center",
        bbox_to_anchor=(x_positions[2], 0.055),
        ncol=3,
        frameon=True,
        fontsize=8,
    )


def plot_disaggregated(metrics_df: pd.DataFrame, output_path: Path, top_n: int, show: bool) -> None:
    import matplotlib.pyplot as plt

    if metrics_df.empty:
        print("No disaggregated metrics to plot.")
        return

    fig, axes = plt.subplots(2, 2, figsize=(16, 12))
    panel_specs = [
        ("coherence", "VV", axes[0, 0]),
        ("coherence", "VH", axes[0, 1]),
        ("covariance", "VV", axes[1, 0]),
        ("covariance", "VH", axes[1, 1]),
    ]

    last_sc = None
    for kind, pol, ax in panel_specs:
        sub = metrics_df[(metrics_df["kind"] == kind) & (metrics_df["pol"] == pol)].copy()
        sc = scatter_panel(
            ax,
            sub,
            title=f"{kind.capitalize()} {pol}",
            top_n=top_n,
            annotate_pareto=(kind == "covariance"),
        )
        if sc is not None:
            last_sc = sc

    fig.suptitle(
        "Disaggregated configuration selection by feature family and polarization",
        fontsize=16,
        fontweight="bold",
        y=0.98,
    )
    add_common_legends(fig, metrics_df, x_positions=(0.25, 0.55, 0.80))
    fig.text(
        0.5,
        0.015,
        "Each point represents one L,W configuration. Color indicates the block-specific score, "
        "marker shape indicates W, and marker size indicates L. Red outlined points belong to the Pareto frontier.",
        ha="center",
        fontsize=10,
    )

    plt.tight_layout(rect=[0, 0.13, 0.94, 0.94])
    if last_sc is not None:
        cbar = fig.colorbar(last_sc, ax=axes.ravel().tolist(), shrink=0.88, pad=0.02)
        cbar.set_label("Block score")

    output_path.parent.mkdir(parents=True, exist_ok=True)
    fig.savefig(output_path)
    if show:
        plt.show()
    plt.close(fig)
    print(f"Per-polarization plot written to: {output_path.resolve()}")


def plot_aggregated(kind_agg_plot: pd.DataFrame, output_path: Path, top_n: int, show: bool) -> None:
    import matplotlib.pyplot as plt

    if kind_agg_plot.empty:
        print("No aggregated metrics to plot.")
        return

    fig, axes = plt.subplots(1, 2, figsize=(16, 7.2))
    last_sc = None

    for kind, ax in zip(["coherence", "covariance"], axes):
        sub = kind_agg_plot[kind_agg_plot["kind"] == kind].copy()
        sc = scatter_panel(
            ax,
            sub,
            title=f"{kind.capitalize()} aggregated over VV and VH",
            top_n=top_n,
            annotate_pareto=(kind == "covariance"),
            table_fontsize=8.5,
            title_fontsize=12,
        )
        if sc is not None:
            last_sc = sc

    fig.suptitle(
        "Aggregated configuration selection by feature family",
        fontsize=16,
        fontweight="bold",
        y=0.98,
    )
    add_common_legends(fig, kind_agg_plot, x_positions=(0.27, 0.57, 0.82))
    fig.text(
        0.5,
        0.015,
        "Each point represents one L,W configuration. Color indicates the aggregated score, "
        "marker shape indicates W, and marker size indicates L. Red outlined points belong to the Pareto frontier.",
        ha="center",
        fontsize=10,
    )

    plt.tight_layout(rect=[0, 0.13, 0.94, 0.94])
    if last_sc is not None:
        cbar = fig.colorbar(last_sc, ax=axes.ravel().tolist(), shrink=0.85, pad=0.02)
        cbar.set_label("Aggregated feature-family score")

    output_path.parent.mkdir(parents=True, exist_ok=True)
    fig.savefig(output_path)
    if show:
        plt.show()
    plt.close(fig)
    print(f"Aggregated plot written to: {output_path.resolve()}")


def build_rankings(metrics_df: pd.DataFrame, kind_agg_plot: pd.DataFrame) -> tuple[pd.DataFrame, pd.DataFrame]:
    disaggregated = (
        metrics_df.sort_values(["kind", "pol", "score"], ascending=[True, True, False])[
            [
                "kind",
                "pol",
                "lookset",
                "window",
                "complementarity",
                "variability",
                "comp_norm",
                "inv_var_norm",
                "score",
                "selected_bands",
            ]
        ]
        if not metrics_df.empty
        else pd.DataFrame()
    )

    aggregated = (
        kind_agg_plot.sort_values(["kind", "score"], ascending=[True, False])[
            [
                "kind",
                "lookset",
                "window",
                "complementarity",
                "variability",
                "comp_norm",
                "inv_var_norm",
                "score",
                "selected_bands",
                "n_polarizations",
                "n_components",
            ]
        ]
        if not kind_agg_plot.empty
        else pd.DataFrame()
    )

    return disaggregated, aggregated


def write_rankings_excel(
    output_path: Path,
    summary_df: pd.DataFrame,
    metrics_df: pd.DataFrame,
    kind_agg_plot: pd.DataFrame,
    disaggregated_ranking: pd.DataFrame,
    aggregated_ranking: pd.DataFrame,
) -> None:
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with pd.ExcelWriter(output_path, engine="xlsxwriter") as writer:
        sheets = {
            "summary_used": summary_df,
            "metrics_disaggregated": metrics_df,
            "metrics_aggregated": kind_agg_plot,
            "ranking_disaggregated": disaggregated_ranking,
            "ranking_aggregated": aggregated_ranking,
        }
        for sheet_name, df in sheets.items():
            df.to_excel(writer, sheet_name=sheet_name, index=False)
            worksheet = writer.sheets[sheet_name]
            worksheet.freeze_panes(1, 0)
            if not df.empty:
                worksheet.autofilter(0, 0, max(len(df), 1), max(len(df.columns) - 1, 0))
                for i, col in enumerate(df.columns):
                    values = df[col].astype(str).replace("nan", "")
                    width = min(max(values.map(len).max(), len(str(col))) + 2, 45)
                    worksheet.set_column(i, i, width)

    print(f"Rankings workbook written to: {output_path.resolve()}")


def configure_plot_style() -> None:
    import matplotlib.pyplot as plt

    plt.rcParams.update(
        {
            "figure.dpi": 120,
            "savefig.dpi": 300,
            "font.size": 10,
            "axes.titlesize": 12,
            "axes.labelsize": 11,
            "axes.grid": True,
            "grid.alpha": 0.25,
            "axes.spines.top": False,
            "axes.spines.right": False,
        }
    )


def run(args: argparse.Namespace) -> None:
    if not args.show:
        import matplotlib

        matplotlib.use("Agg")

    load_optional_adjust_text()
    configure_plot_style()

    summary_df = load_summary(args.metrics_xlsx, exclude_l2=not args.include_l2)
    metrics_df = build_metrics_df(
        summary_df,
        weight_complementarity=args.weight_complementarity,
        weight_variability=args.weight_variability,
    )
    kind_agg_plot = build_kind_aggregation(metrics_df)
    disaggregated_ranking, aggregated_ranking = build_rankings(metrics_df, kind_agg_plot)

    output_dir = args.output_dir
    plot_disaggregated(metrics_df, output_dir / args.per_pol_png, top_n=args.top_n, show=args.show)
    plot_aggregated(kind_agg_plot, output_dir / args.agg_png, top_n=args.top_n, show=args.show)
    write_rankings_excel(
        output_path=output_dir / args.rankings_xlsx,
        summary_df=summary_df,
        metrics_df=metrics_df,
        kind_agg_plot=kind_agg_plot,
        disaggregated_ranking=disaggregated_ranking,
        aggregated_ranking=aggregated_ranking,
    )

    if not HAS_ADJUSTTEXT:
        print("Optional package adjustText is not installed; Pareto labels were not auto-adjusted.")


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Generate plots and rankings from coherence/covariance metrics."
    )
    parser.add_argument(
        "--metrics-xlsx",
        type=Path,
        default=Path("coherence_covariance_metrics.xlsx"),
        help="Input metrics workbook produced by compute_coherence_covariance_metrics.py.",
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=Path("."),
        help="Directory for PNG plots and ranking workbook.",
    )
    parser.add_argument("--per-pol-png", default="Per-pol.png", help="Filename for the per-polarization plot.")
    parser.add_argument("--agg-png", default="Agg.png", help="Filename for the aggregated plot.")
    parser.add_argument(
        "--rankings-xlsx",
        default="configuration_rankings.xlsx",
        help="Filename for the output rankings workbook.",
    )
    parser.add_argument(
        "--weight-complementarity",
        type=float,
        default=0.5,
        help="Score weight for normalized complementarity.",
    )
    parser.add_argument(
        "--weight-variability",
        type=float,
        default=0.5,
        help="Score weight for inverse normalized variability.",
    )
    parser.add_argument("--include-l2", action="store_true", help="Include L2 configurations.")
    parser.add_argument("--top-n", type=int, default=5, help="Top configurations shown in plot insets.")
    parser.add_argument("--show", action="store_true", help="Show figures interactively in addition to saving them.")

    args = parser.parse_args()
    if args.weight_complementarity < 0 or args.weight_variability < 0:
        parser.error("Weights must be non-negative.")
    if np.isclose(args.weight_complementarity + args.weight_variability, 0):
        parser.error("At least one weight must be positive.")
    if args.top_n < 1:
        parser.error("--top-n must be at least 1.")

    total = args.weight_complementarity + args.weight_variability
    args.weight_complementarity = args.weight_complementarity / total
    args.weight_variability = args.weight_variability / total
    return args


def main() -> None:
    args = parse_args()
    run(args)


if __name__ == "__main__":
    main()
